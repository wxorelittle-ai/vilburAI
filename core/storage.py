"""
Хранилище статики с хешированными именами: app.<хеш>.css вместо app.css.

Зачем. Раньше CSS отдавался по вечному адресу /static/css/app.css, а nginx клеил
ему `Cache-Control: public, immutable; expires 30d`. `immutable` означает «файл
никогда не изменится, даже не проверяй» — и браузер до 30 дней держал старую копию.
Поверх этого service worker кэширует статику cache-first по тому же адресу. Итог:
правки вёрстки не доезжали до тех, кто уже открывал сайт (проверено на бою: сервер
отдавал 24 291 байт, браузер брал из кэша 21 930 — без новых классов).

Хеш в имени решает это в корне: изменился файл — изменился адрес — кэш промахнулся
и скачал новое. А `immutable` на 30 дней становится честным: конкретный
app.<хеш>.css и правда неизменен.

manifest_strict=False: без него любой {% static %} на файл, которого нет в манифесте,
роняет рендер ошибкой. Манифест появляется только после collectstatic, поэтому в
тестах и при локальной разработке строгий режим ломал бы страницы на ровном месте.
Со strict=False адрес просто остаётся нехешированным.
"""

from django.contrib.staticfiles.storage import ManifestStaticFilesStorage


class TerpimoeManifestStorage(ManifestStaticFilesStorage):
    manifest_strict = False
