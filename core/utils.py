"""Общие мелочи, нужные нескольким модулям."""


def klientskiy_ip(request) -> str:
    """
    IP клиента за Nginx.

    Берём X-Real-IP: наш прокси проставляет его из $remote_addr и перезаписывает
    любое клиентское значение — ему можно верить. X-Forwarded-For НЕ используем:
    nginx лишь дописывает реальный адрес справа, а слева клиент может подставить
    что угодно, и наивное чтение первого элемента даёт подделку.

    Важно для ПЭП (IP — часть доказательства подписи, раздел «Модуль I» ТЗ)
    и для проверки отправителя вебхука ЮKassa.
    """
    return (request.META.get('HTTP_X_REAL_IP')
            or request.META.get('REMOTE_ADDR')
            or '')
